//BUCLES

var contador = 0;
var contador2 = 0;


// MIENTRAS (WHILE)

while(contador<10){
    console.log("El contador WHILE esta en: ",contador)
    contador+=1
}

while(contador2<10){
    contador2+=1
    console.log("El contador2 WHILE esta en: ",contador2)
}



// POR O PARA (FOR)

for(var contador3 = 0; contador3<10; contador3+=1){
    console.log("El contador3 FOR esta en: ",contador3)
}